                    {
                        var newTile = new Tile(texture, sourceRectangles, 54);
                        newTile.X = newTile.Width * column + newTile.Width / 2;
                        newTile.Y = newTile.Height * row + newTile.Width / 2;
                        newTile.IsVisible = false;
                        innerWall[row, column] = newTile;
                    }
                }
            }
        }

        public void Draw(SpriteBatch spriteBatch)
        {
            for (int row = 0; row < outerWallData.GetLength(0); row++)
            {
                for (int column = 0; column < outerWallData.GetLength(1); column++)
                {
                    if (innerWall[row, column] != null)
                        innerWall[row, column].Draw(spriteBatch);
                    if (outerWall[row, column] != null)
                        outerWall[row, column].Draw(spriteBatch);
                }
            }
        }
    }
