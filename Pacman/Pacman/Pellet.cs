        public static List<Pellet> CreateAllPellets(Map map)
        {
            var pellets = new List<Pellet>();
            for (int row = 0; row < pelletMap.GetLength(0); row++)
            {
                for (int column = 0; column < pelletMap.GetLength(1); column++)
                {
                    if (pelletMap[row, column] > 0)
                    {
                        Pellet pellet;
                        if (pelletMap[row, column] == 1)
                            pellet = new Pellet();
                        else
                            pellet = new Pellet(isPowerPellet: true);
                        pellet.Position = map.Tiles[column, row].Position;
                        pellets.Add(pellet);
                    }
                }
            }
            return pellets;
        }

