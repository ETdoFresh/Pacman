        public MapCell[,] cells;
        public int MapWidth = innerWallData.GetLength(1);
        public int MapHeight = innerWallData.GetLength(0);
        public int TileWidth;
        public int TileHeight;
        
        private Texture2D texture;
        private List<Rectangle> sourceRectangles;

        public TileMap(Texture2D texture, List<Rectangle> sourceRectangles)
        {
            this.texture = texture;
            this.sourceRectangles = sourceRectangles;

            TileWidth = sourceRectangles[54].Width;
            TileHeight = sourceRectangles[54].Height;

            cells = new MapCell[MapWidth, MapHeight];
            for (int x = 0; x < MapWidth; x++)
            {
                for (int y = 0; y < MapHeight; y++)
                {
                    cells[x, y] = new MapCell(innerWallData[y, x] == 0 ? 0 : innerWallData[y, x] + 53);
                    cells[x, y].Orientation = innerWallOrientation[y, x] * MathHelper.ToRadians(90);
                    
                    var secondTile = outerWallData[y, x] == 0 ? 0 : outerWallData[y, x] + 53;
                    var secondOrientation = outerWallOrientation[y, x] * MathHelper.ToRadians(90);
                    cells[x, y].Add(secondTile, secondOrientation);
                }
            }
        }

