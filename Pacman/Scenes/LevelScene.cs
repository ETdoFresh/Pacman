    class LevelScene : SceneObject
    {
        TileGrid _tileGrid;
        Objects.Pacman _pacman;
        DisplayObject _target;
        Random _random;

        public LevelScene()
            : base("Level")
        {
            _random = new Random();
            _tileGrid = new TileGrid(outerWallData.GetLength(1), outerWallData.GetLength(0), 15, 15);
            for (int x = 0; x < _tileGrid.NumberOfXTiles; x++)
            {
                for (int y = 0; y < _tileGrid.NumberOfYTiles; y++)
                {
                    var rect = new RectangleObject(_tileGrid.TileWidth, _tileGrid.TileHeight);
                    rect.Translate(_tileGrid.Data[x, y].PositionVector.X, _tileGrid.Data[x, y].PositionVector.Y);
                    rect.Tint = new Color(_random.Next(0, 255), _random.Next(0, 255), _random.Next(0, 255));
                    rect.Alpha = 0.3f;
                    AddChild(rect);
                }
            }

            SetupBoard();
            _pacman = new Objects.Pacman();
            _pacman.Translate(25, 25);
            AddChild(_pacman);

            _target = new CircleObject(15);
            _target.Translate(400, 25);
            _target.Tint = Color.Red;
            _target.Alpha = 0.4f;
            AddChild(_target);

            _pacman.SteerTowards(_target);
        }

        public override void Update(GameTime gameTime)
        {
            base.Update(gameTime);
            if (InputHelper.IsPressed(Keys.Space) || InputHelper.IsPressed(Keys.Escape))
            {
                Stage.GotoScene("Menu");
            }
            else if (InputHelper.IsPressed(Keys.T))
            {
                Stage.GotoScene("AnimationTest");
            }

            if (InputHelper.GetInputState(MouseButton.Left) == InputState.Pressed || InputHelper.GetInputState(MouseButton.Left) == InputState.Hold)
                _target.Translate(InputHelper.MouseX, InputHelper.MouseY);
        }

        private void SetupBoard()
        {
            const int indexOffset = 53;
            var board = new GroupObject();
            AddChild(board);

            for (var row = 0; row < outerWallData.GetLength(0); row++)
            {
                for (var column = 0; column < outerWallData.GetLength(1); column++)
                {
                    var tileGroup = new GroupObject();
                    board.AddChild(tileGroup);

                    if (outerWallData[row, column] > 0)
                    {
                        var outerTile = new SpriteObject("pacman", outerWallData[row, column] + indexOffset);
                        outerTile.SetOrigin(7.5f, 7.5f);
                        outerTile.Translate(column * 15 + 7, row * 15 + 7);
                        outerTile.Rotate(outerWallOrientation[row, column] * 90);
                        outerTile.Resize(1.05f);
                        tileGroup.AddChild(outerTile);
                    }

                    if (innerWallData[row, column] > 0)
                    {
                        var innerTile = new SpriteObject("pacman", innerWallData[row, column] + indexOffset);
                        innerTile.SetOrigin(7.5f, 7.5f);
                        innerTile.Translate(column * 15 + 7, row * 15 + 7);
                        innerTile.Rotate(innerWallOrientation[row, column] * 90);
                        innerTile.Resize(1.05f);
                        tileGroup.AddChild(innerTile);
                    }
                }
            }
        }

